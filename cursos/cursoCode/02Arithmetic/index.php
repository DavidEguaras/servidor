<?php
    // Arithmetic operators
    // + - * / ** %

    // Increment, decrement operators
    // ++, --

    // Operator Precedence
    // ()
    // **
    // * / %
    // + -

    $x = 10;
    $y = 2;
    $z = null;

    // Suma
    $z = $x + $y;
    echo "Suma: " . $z . "<br>";

    // Resta
    $z = $x - $y;
    echo "Resta: " . $z . "<br>";

    // Multiplicación
    $z = $x * $y;
    echo "Multiplicación: " . $z . "<br>";

    // División
    $z = $x / $y;
    echo "División: " . $z . "<br>";

    // Exponenciación
    $z = $x ** $y;
    echo "Exponenciación: " . $z . "<br>";

    // Módulo
    $z = $x % $y;
    echo "Módulo: " . $z . "<br>";

    // Incremento
    $z = ++$x;
    echo "Incremento de x: " . $z . "<br>";

    // Decremento
    $z = --$x;
    echo "Decremento de x: " . $z . "<br>";
?>
